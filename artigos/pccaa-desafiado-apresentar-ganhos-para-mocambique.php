 			<p>O Ministro dos Transportes e Comunicações, Dr. Mateus Magala, desafiou as Unidades de Coordenação e todos os intervenientes na implementação do Projecto de Comércio e Conectividade da África Austral (PCCAA) a identificarem oportunidades de ganhos de curto e médio prazos para os Moçambicanos dentro do escopo e actividades previstas no projecto. 
                A declaração foi feita na ocasião de abertura de uma Missão do Banco Mundial, a 13 de fevereiro de 2023, realizada com o objectivo de apoiar a implementação do PCCAA. 
                Na ocasião, o Ministro Mateus Magala reconheceu o encontro como uma oportunidade para discutir o progresso do Projecto, assinalando a necessidade de uma gestão eficiente dos recursos disponíveis em busca de produzir resultados tangíveis, que se mostram ainda mais urgentes num momento em que o país enfrenta as nefastas consequências causadas pelas chuvas fortes registadas no país. 
                Por sua vez, Ankur Huria, Especialista Sénior do Banco Mundial e Líder da Equipa de Implementação (TTL) do PCCAA, acolheu o desafio lançado pelo governante, afirmando o compromisso de trabalhar arduamente para, junto das equipas envolvidas na implementação, identificar mais oportunidades para providenciar resultados positivos para o país dentro do escopo do Projecto. Como resultado do compromisso, as Unidades de Coordenação do PCCAA no Ministério dos Transportes e Comunicações (MTC) e na Administração Nacional de Estradas (ANE) foram instadas pelo TTL a concentrar esforços com vista a acelerar as actividades passíveis de prover resultados imediatos como forma de maximizar os benefícios a nível nacional e regional.
                A referida missão foi a terceira do Banco Mundial desde a efectividade do Projecto, e a primeira do ano de 2023, que decorreu ao longo de 3 semanas, nas quais foram realizadas diversas sessões de trabalho com os intervenientes na implementação, de forma a avaliar o estágio das actividades e encontrar soluções para uma maior flexibilização e eficiência na sua execução.
                Ainda no âmbito da Missão, uma equipa do Banco Mundial liderada por Lauren Corthay escalou a província de Tete nos dias 27 e 28 de fevereiro com vista a trabalhar, com especialistas da UCP do MTC, da Agência de Desenvolvimento do Vale do Zambeze (ADVZ) e do Fundo Catalítico para a Inovação e Desenvolvimento (FCID), nas actividades da componente III do projecto. 
                Ainda em Tete, para além de uma reunião realizada em formato híbrido nos escritórios da ADVZ, a Missão visitou a Unidade de Abate e Processamento Industrial de Carne da CanelFood, na qualidade de beneficiário do financiamento da edição anterior do Fundo, para uma apresentação da unidade e conhecimento do seu funcionamento, tendo igualmente visitado o local em que será erguida a Expo-Zambeze, no qual foi realizada uma apresentação do Projecto para melhor entendimento da equipa sobre os contornos que ditarão a construção da feira.


