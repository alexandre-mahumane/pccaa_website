 			<p>No âmbito da implementação do Projecto de Comércio e Conectividade da África Austral (PCCAA), e em cumprimento da recomendação emanada da quinta reunião do Comité Técnico Conjunto Tripartido, a Comissão multissectorial de Moçambique para o estabelecimento de fronteiras de paragem única deslocou-se ao posto fronteiriço de Chanida, na Zâmbia, no passado dia 29 de Setembro do ano corrente, para dar seguimento aos trabalhos de preparação da proposta de acordo bilateral com vista à implementação da Fronteira de Paragem Única de Cassacatiza-Chanida.
			Chefiada por Beatriz Machava, da Direcção Nacional de Comércio Externo – Ministério da Indústria e Comércio, a delegação moçambicana foi recebida em Chanida por Raphael Phiri, representante do Secretário Permanente do Distrito de Chadiza, chefe da delegação zambiana. Phiri manifestou à congénere moçambicana o forte interesse do seu país no sucesso do encontro, ressaltando a importância da implementação do projecto de Fronteira de Paragem Única para as duas nações que serão economicamente beneficiadas e desenvolvidas com o avanço do projecto.
			O encontro que visava essencialmente permitir a aferição das condições de estabelecimento do posto de fronteira de paragem única, troca de impressões entre as duas comissões sobre o tipo de fronteira a ser adoptado e sobre o modelo de funcionamento, contou inicialmente com uma apresentação à delegação moçambicana da primeira fase das obras de melhoramento da infraestrutura do posto fronteiriço de Chanida, a qual beneficiou de trabalhos de melhoramento devido a um crescimento substancial do movimento de travessia e consequente congestionamento, suscitando a necessidade de aprimorar a infraestrutura para uma nova realidade.
			Por sua vez, a delegação zambiana visitou as infraestruturas do posto de travessia de Cassacatiza, onde foram apresentados os processos e procedimentos adoptados tanto pelas autoridades migratórias como pelas alfandegárias. Ainda em território moçambicano, a delegação zambiana teve igualmente a oportunidade de conhecer as melhorias realizadas no posto fronteiriço, de modo a permitir uma melhoria da qualidade de resposta à demanda registada.
			O encontro serviu igualmente para analisar e discutir as propostas submetidas aos dois países, incluindo as notas conceptuais relativas aos processos e procedimentos propostos e metodologia de financiamento para o avanço da implementação do projecto. Prevê-se ainda a continuação dos trabalhos, na perspectiva de que os acordos, para além de aprovados, sejam assinados pelos Chefes de Estado dos dois países ainda no ano de 2022. 
			Ressalve-se que, em Moçambique, a Comissão Multissectorial para o estabelecimento de fronteiras de paragem única é composta pela Autoridade Tributária (AT), Serviço Nacional de Migração (SENAMI), Ministério da Indústria e Comércio (MIC), Ministério da Defesa Nacional (MDN), Ministério da Agricultura e Desenvolvimento Rural (MADER), Ministério dos Transportes e Comunicações (MTC), Ministério dos Negócios Estrangeiros e Cooperação (MINEC), Ministério da Saúde (MISAU) e Ministério das Obras Públicas, Habitação e Recursos Hídricos (MOPHRH) representada pela Administração de Estradas (ANE), tendo nesta ocasião feito parte da mesma a Unidade de Coordenação do PCCAA. 
