 			<p>Moçambique e Malawi marcaram mais um passo importante na implementação de Fronteiras de Paragem Única com a realização, nos dias 13 e 14 de Março, na Cidade de Tete, da primeira reunião do Comité Técnico para a Implementação de Postos de Fronteira de Paragem Única (PFPU) entre os dois países, no âmbito da operacionalização do Projecto de Comércio e Conectividade da África Austral (PCCAA).
                Dando seguimento ao Acordo Bilateral assinado entre Moçambique e o Malawi, em 2021, para a criação de Postos de Fronteira de Paragem Única  entre os dois países, nomeadamente nas fronteiras de Milange-Muloza, Zóbuè-Muanza, Calómuè-Dedza e Mandimba-Chiponde, a reunião do Comité Técnico tinha como objectivo discutir o conteúdo do Acordo Bilateral, harmonizar os passos a seguir para a aceleração da implementação da Componente 2 do Projecto por parte de Moçambique, bem como debater sobre a operacionalização dos Postos de Fronteira de Paragem Única e medidas para colmatar as inundações no Posto de Fronteira de Milange-Muloza, onde também se pretende instalar uma Fronteira de Paragem Única.
                Renato Furruma, Director Nacional de Operações dos Serviços Nacionais de Migração, Chefe da delegação Moçambicana tomou a oportunidade para reconhecer a dedicação dos delegados para o sucesso do encontro, recordando que há ainda mais trabalho a ser feito no espírito de fraternidade para garantir que o comércio fragmentado pelos dois povos e pela Região seja facilitado.
                Por sua vez, o co-presidente do encontro, Sr. Mufwa Munthali, Director do Comércio de Serviços do Ministério do Comércio e Indústria do Malawi, expressou o seu apreço pela recepção calorosa da República de Moçambique, congratulando a reunião pelo bom trabalho demonstrado pelas duas delegações durante as actividades. Munthali referiu ainda que o compromisso demonstrado reflecte a determinação dos dois governos em facilitar o comércio para os dois povos, assinalando que as próximas etapas para a finalização do Plano de Ação são fundamentais para a sua operacionalização.
                A Reunião do Comité Técnico para a Implementação de Postos de Fronteira de Paragem Única contou com a participação dos Coordenadores das Unidades de Coordenação do PCCAA nos dois países, de técnicos de ambos países nos sectores dos Transportes e Comunicações, Indústria e Comércio, Migração, Alfândegas, entre outros, tendo permitido a partilha de experiências entre os dois países em áreas diversas, incluindo processos e procedimentos comerciais, alfandegários e de implementação de PFPU.
                De acordo com os participantes, a primeira Reunião do Comité Técnico para a Implementação de Fronteiras de Paragem Única entre Moçambique e o Malawi ultrapassou as expectativas, tendo decorrido num ambiente de concórdia entre as partes, e logrado a finalização do Quadro Estratégico entre os dois países, assim como os Termos de Referência dos Comités Nacionais para a Implementação dos Postos de Fronteira de Paragem Única e o Mapeamento do caminho a seguir para abordar a área de inundação do projecto do Posto de Fronteira de Paragem Única de Milange-Muloza.

