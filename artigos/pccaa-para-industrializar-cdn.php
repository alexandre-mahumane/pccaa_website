 			<p>O Ministro dos Transportes e Comunicações, Mateus Magala, avançou que o governo de Moçambique vê o Projecto de Comércio e Conectividade da África Austral (PCCAA), que está a ser implementado em conjunto por Moçambique, Malawi e Zâmbia, com financiamento do Grupo Banco Mundial, como alavanca para transformar o Corredor de Desenvolvimento de Nacala num corredor de industrialização, ao criar oportunidades para que o mesmo seja mais competitivo e atraia mais investimentos estrangeiros.
			A informação foi avançada pelo Ministro aquando da visita de trabalho ao Corredor de Nacala, nos dias 17 e 18 de Outubro corrente, na qual se fazia acompanhar por Jacob Hara e Samuel Kawale, Ministros dos Transportes e Obras Públicas e da Terra e Planeamento Urbano da República do Malawi, respectivamente. 
			A visita tinha como objectivo inteirar-se do funcionamento das infra-estruturas e serviços de logística de importação e exportação das mercadorias de e para o Malawi, através dos corredores moçambicanos, particularmente o de Nacala.
			Durante a visita, os Ministros dos dois países visitaram as principais infra-estruturas ferro-portuárias do corredor, mantiveram conversações sobre os serviços de transporte e logística prestados por Moçambique ao Malawi, bem como realizaram encontros com empresários e autoridades locais, o que permitiu identificar os principais desafios para dinamizar o Corredor de Desenvolvimento de Nacala.
			Prestando declarações à imprensa, o Ministro dos Transportes e Obras Públicas do Malawi, Jacob Hara, asseverou a sua satisfação pelo nível de investimento feito pelo governo moçambicano na reabilitação do porto de Nacala, reconhecendo a crucialidade da implementação de projectos da natureza para a facilitação do comércio no seu país e noutros da região e realçando a importância de iniciativas de financiamento como a do Banco Mundial, através do PCCAA, que contribuirão, certamente, para o desenvolvimento socioeconómico da região.
			Ainda sobre o PCCAA, Mateus Magala avançou que o projecto é de suma importância no processo de elevação da competitividade do corredor, visto que o financiamento de 230 Milhões do Grupo Banco Mundial será implementado em áreas que constituem a espinha dorsal do corredor, com especial destaque para a facilitação do acesso dos produtores aos centros de comércio, como também a remoção de barreiras ao comércio entre os países envolvidos na sua implementação. Refira-se que os Ministros do Malawi visitaram o Corredor de Nacala depois de terem trabalhado, na passada sexta-feira, dia 14 de Outubro corrente, no Porto da Beira que manuseia carga de e para o Malawi, particularmente fertilizantes, integrados na implementação do programa presidencial malawiano de subsídios de insumos de produção.
