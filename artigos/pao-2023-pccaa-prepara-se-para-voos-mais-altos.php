 			<p>A menos de 2 meses de completar o seu primeiro ano de actividades, o Projecto de Comércio e Conectividade da África Austral (PCCAA) reuniu, de 19 a 23 de Setembro de 2022, as Unidades de Coordenação do Ministério dos Transportes e Comunicações (MTC) e da Administração Nacional de Estradas (ANE) e os pontos focais das diversas instituições beneficiárias para a elaboração e harmonização do Plano de Actividades e Orçamento para o ano de 2023.
			Das intensas sessões de trabalho em equipa, marcadas por diversas discussões e actividades intersectoriais, resultou um documento que orientará as realizações do Projecto durante o ano de 2023, tendo em conta as suas 4 componentes que envolvem a redução dos custos comerciais por via da facilitação do comércio na região, incluindo infraestruturas adequadas de fronteira e reformas regulatórias, fortalecimento da coordenação regional e apoio na implementação do projecto, aumento de investimentos nas cadeias de suprimentos inerentes à região, e fortalecimento das infraestruturas de transporte para melhorar o acesso aos mercados, respectivamente.
			O encontro foi orientado pelo Coordenador do Projecto, Benjamin Kerchan, e permitiu analisar o nível de realização das actividades planificadas para o ano de 2022 até à data, actividades essas que envolvem contratação de consultores, firmas, e demais provedores de serviços para o arranque das  actividades principais do Projecto, assim como a realização de encontros entre os principais intervenientes, assinatura de acordos, entre outas actividades. 
			Baseado na análise feita, o coordenador do projecto, Benjamin Kerchan, recordou aos presentes sobre a crucialidade do avanço nas actividades ainda em tempo de realização em 2022, tendo igualmente endereçado um especial apelo para um contínuo e maior comprometimento por parte de todos os intervenientes para que os níveis de desempenho do PCCAA sejam melhorados ainda este ano, de modo a que tal se reflicta e continue a reflectir-se nos 5 anos subsequentes previstos para a implementação do mesmo.
			De entre as actividades planeadas para realização, no âmbito do projecto no ano de 2023, destacam-se reformas regulatórias em matéria de comércio transfronteiriço, construção/reabilitação de estradas, reassentamentos nas áreas abrangidas pelas obras de estradas e implantação de fronteiras, financiamento de iniciativas privadas para o aumento da produção e produtividade, monitorização dos indicadores de desempenho do corredor de Nacala, entre outras. 

			Note-se que continua prevista ainda para o corrente ano, a realização das actividades planificadas para o último trimestre, que se espera que tragam um contributo significativo para o crescimento dos níveis de desembolso para o primeiro ano do projecto e, consequentemente, o avanço na implementação das actividades distribuídas pelas suas quatro componentes.
