 			<p>O Projecto de Comércio e Conectividade da África Austral (PCCAA) veio auxiliar o governo no processo de capacitação e facilitação do processo de inspecção e fiscalização sanitária nas fronteiras do país, segundo Florência Cipriano, Directora Nacional de Sanidade Agro-pecuária e Biossegurança (DNSAB) do Ministério da Agricultura e Desenvolvimento Rural (MADER), que falava à margem de uma formação em boas práticas para a prevenção e controlo de doenças animais, realizada de 28 de Novembro a 01 de Dezembro, no distrito de Marracuene, na província de Maputo.
                Florência Cipriano falava para um total de 10 técnicos do MADER afectos a diversos pontos, numa formação realizada pela direcção que dirige, ao longo de 4 dias, tomando o ensejo para mencionar a suma importância da implementação do PCCAA em Moçambique, uma vez que através do mesmo financiamento foi possível a realização da referida capacitação aos inspectores agro-pecuários.
                O evento, enquadrado no âmbito da componente I do PCCAA, relativa à redução dos custos das trocas comerciais através da facilitação do comércio, consistia em capacitar os participantes inerentemente ao controlo de doenças transfronteiriças, medicamentos veterinários e controlo veterinário de fronteiras com o objectivo de fortalecer o seu conhecimento e habilidades em matérias de vigilância, assim como garantir maior eficácia e eficiência no controlo de doenças transfronteiriças e medicamentos veterinários e na realização de inspecção de circulação interna e fronteiriça.
                Abordados sobre a pertinência e os efeitos verificados em resultado da participação no evento, os inspectores mostraram-se satisfeitos pela oportunidade de participar da formação, vendo-se, depois dela, mais habilitados sobre a legislação relacionada com o controlo de circulação interna e fronteiriço, fitossanitário e veterinário, procedimentos de trânsito interno e fronteiriço, requisitos legais sobre sanidade vegetal e sanidade animal, influência das doenças transfronteiriças na saúde pública e na economia.
                Por sua vez, os formadores foram unânimes em classificar a formação como de suma importância, uma vez que os conhecimentos transmitidos permitirão o estabelecimento e organização de um sistema de registo de dados sobre o controlo fitossanitário e veterinário de circulação interna e de fronteiras rastreável e credível, a realização de acções de análise de risco que possibilitem a tomada de decisões no acto de inspecção nas fronteiras, como também a socialização do processo organizativo para o controlo de medicamentos. 
                Ressalve-se que a Direcção Nacional de Sanidade Agro-pecuária e Biossegurança, responsável pela facilitação da formação, é a entidade do MADER cujas incumbências incluem a garantia do  controlo  hígiossanitário  dos  produtos  de origem animal, o estabelecimento de mecanismos de vigilância de doenças dos animais com impacto na economia e na saúde pública, a garantia do cumprimento das medidas de defesa sanitária e bem-estar animal e os processos de certificação veterinária, zelo pelo cumprimento da legislação sobre produção pecuária e sanidade animal, assegurar a protecção e defesa fitossanitária, salvaguardando a saúde pública e o meio ambiente, fiscalização e inspecção das redes comerciais de sementes, fertilizantes e pesticidas, colaboração com os organismos regionais e internacionais no âmbito da vigilância, prevenção e controlo zoo-fitossanitário, entre outras.

