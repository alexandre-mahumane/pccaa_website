 			<p>Depois de uma série de visitas realizadas em Junho último por equipas multidisciplinares  da Administração Nacional de Estradas (ANE), Ministério dos Transportes e Comunicações (MTC), Autoridade Tributária (AT) e Serviço Nacional de Migração (SENAMI), para a demarcação das áreas onde deverão ser construídas as fronteiras de paragem única tendo em vista o inicio do  processo de licenciamento ambiental, no âmbito da implementação do Projecto de Comércio e Conectividade da África Austral (PCCAA), foram realizados, em Agosto do ano corrente, os trabalhos de  pré-avaliação ambiental e social.
			A missão realizada nas duas províncias visava a aferição dos espaços nos quais estão em funcionamento as actuais infra-estruturas, a verificação dos limites das áreas alocadas para a implantação das novas infraestruturas fronteiriças, com vista a categorização ambiental e definição do tipo de estudo ambiental a ser elaborado. Para o efeito, o trabalho consistiu na  identificação dos potenciais impactos socioambinetais  que possam advir da implantação da actividade incluindo a possível ocorrência de conflitos e suscpetibilidade de reassentamento involuntário. 
			Para a província da Zambézia, no distrito de Milange, a actividade foi realizada por uma equipa composta por técnicos dos Serviços Provinciais de Ambiente (SPA), ANE, MTC, Unidade de Coordenação do Projecto (UCP) do PCCAA e das estruturas de administração local, incluindo Representantes da AT, SENAMI e Guarda fronteira, tendo consistido numa visita guiada pela área da reserva do Estado, na qual se prevê a implantação da fronteira de paragem única, tendo sido constatada a existência de condições socioambientais favoráveis para a sua construção sem implicar a  necessidade de reassentamento involuntário.
			Já em Tete,  para além dos técnicos da ANE, MTC, SPA, UCP-PCCAA, a equipa contou com o apoio dos respectivos  chefes dos postos do Serviço Nacional de Migração (SENAMI) e da Autoridade Tributária (AT), tendo sido constatado que os locais propostos reúnem as condições ambientas  e sociais para a implantação dos postos Fronteiriços tendo em conta as exigências do regulamento ambiental e as políticas de Salvaguardas do Banco Mundial. Contudo, a área proposta para a construção do novo Posto de Zóbuè acarretará uma intervenção adicional, uma vez que foi  constatada a necessidade de reassentamento das populações ali residentes.
			A mesma missão, na província de Tete, contou com a participação de um técnico do PCCAA para efectuar o levantamento das condições e necessidades do ponto de vista de Tecnologias de Informação e Comunicação dos postos de travessia para o seu devido melhoramento no âmbito do projecto, que constantou a necessidade de assegurar o melhoramento das condições de acesso à internet para os dois postos visitados com máxima urgência devido à obsolência e morosidade causadas pela actual instabilidade da conexão.
			Ressalve-se que, no âmbito do Projecto, antes das visitas realizadas para a pré-avaliação ambiental e social, técnicos da ANE também fizeram-se aos mesmos locais  e, com recurso a drones, sobrevoaram a área a fim de obtenção de informação mais detalhada para o  mapeamento das condições topográficas, hídricas, geológicas assim como dos aspectos  socio-económicos das áreas em que se pretente construir as infra-estruturas fronteiteiriças.
